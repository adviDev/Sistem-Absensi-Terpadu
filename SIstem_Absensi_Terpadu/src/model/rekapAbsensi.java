package model;
public class rekapAbsensi {
    private String nim;
    private int totalHadir;
    private int totalSakit;
    private int totalIzin;
    private int totalAlpa;
    
    
    public rekapAbsensi() {
    }

    public String getNim() {
        return nim;
    }

    public void setNim(String nim) {
        this.nim = nim;
    }

    public int getTotalHadir() {
        return totalHadir;
    }

    public void setTotalHadir(int totalHadir) {
        this.totalHadir = totalHadir;
    }

    public int getTotalSakit() {
        return totalSakit;
    }

    public void setTotalSakit(int totalSakit) {
        this.totalSakit = totalSakit;
    }

    public int getTotalIzin() {
        return totalIzin;
    }

    public void setTotalIzin(int totalIzin) {
        this.totalIzin = totalIzin;
    }

    public int getTotalAlpa() {
        return totalAlpa;
    }

    public void setTotalAlpa(int totalAlpa) {
        this.totalAlpa = totalAlpa;
    }
    
    
}
